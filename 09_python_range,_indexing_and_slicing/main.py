numbers = range(10) # start 0; stop (10-1); step 1
print(list(numbers))

number = range(2,14) # start 2; stop (14-1); step 1
print(list(number))

number = range(4,15,2) # start 4; stop (15-1); step 2
print(list(number))

print("===Indexing====")
num = list(range(3,10))
print(num)

print(num[2])
print(num[-1])
print(num[-3])

print("===Slicing====")
angka = list(range(1,101))
print(angka)

print(angka[:10]) # index 0 sampai index 9

print(angka[3:10]) # index 3 sampai index 9

print(angka[4:15:2]) #index 4 sampai index 14 step 2

print(angka[10:]) # index 10 sampai index terakhir

print(angka[-10:]) # 10 data terakhir





