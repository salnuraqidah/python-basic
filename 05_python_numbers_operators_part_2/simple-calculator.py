num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

add = num1 + num2
sub = num1 - num2
kali = num1 * num2
bagi = num1 / num2

print(f"{num1} + {num2} = {add}")
print(f"{num1} - {num2} = {sub}")
print(f"{num1} x {num2} = {kali}")
print(f"{num1} / {num2} = {bagi}")


