number1 = 10
number2 = 3

add = number1 + number2
print(f"{number1} + {number2} = {add}")

subs = number1 - number2
print(f"{number1} - {number2} = {subs}")

multiply = number1 * number2
print(f"{number1} x {number2} = {multiply}")

div = number1 / number2
print(f"{number1} / {number2} = {div}")

floor_div = number1 // number2
print(f"{number1} // {number2} = {floor_div}")

expo = number1 ** number2
print(f"{number1} ^ {number2} = {expo}")

mod = number1 % number2
print(f"{number1} % {number2} = {mod}")

print("Assignment Operator")
a = 5
a+=3 # a = a + 3
print(a)

a-=4 # a = a - 4
print(a)

a*=7 # a = a x 7
print(a)
